# This sample is built using the handler classes approach in skill builder.
import logging
import ask_sdk_core.utils as ask_utils

from ask_sdk_core.skill_builder import SkillBuilder
from ask_sdk_core.dispatch_components import AbstractRequestHandler
from ask_sdk_core.dispatch_components import AbstractExceptionHandler
from ask_sdk_core.handler_input import HandlerInput

from ask_sdk_model import Response

logger = logging.getLogger(__name__)
logger.setLevel(logging.INFO)


class LaunchRequestHandler(AbstractRequestHandler):
    """Handler for Skill Launch."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool

        return ask_utils.is_request_type("LaunchRequest")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        speak_output = "Welcome to Numbi Converter. You can convert any number to Binary format."

        return (
            handler_input.response_builder
                .speak(speak_output)
                .ask(speak_output)
                .response
        )


class BinaryNumIntentHandler(AbstractRequestHandler):
    """Handler for Hello World Intent."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool
        return ask_utils.is_intent_name("BinaryNumIntent")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        binary_num = handler_input.request_envelope.request.intent.slots["number"].value
        binary_int = int(binary_num)
        def Binary(n): 
        	s = bin(n) 
        	
        	# removing "0b" prefix 
        	s1 = s[2:] 
        	return s1 
        
        binary_output = Binary(binary_int)

        speak_output = "This is your binary number {}. ".format(binary_output)
        reprompt_output = " Want to change another number?"

        return (
            handler_input.response_builder
                .speak(speak_output + reprompt_output)
                .ask(reprompt_output)
                .response
        )


